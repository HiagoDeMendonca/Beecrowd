import java.util.Scanner;

public class Main {
    
    static Scanner scan = new Scanner(System.in);
 
    public static void main(String[] args) {
        
        int A, B, X;
        
        A = scan.nextInt();
        B = scan.nextInt();
        
        X = A + B;
        
        System.out.println("X = " + X);
    }
    
}
